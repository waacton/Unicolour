namespace Wacton.Unicolour;

public class Spd : Dictionary<int, double>
{
    /*
     * SPD white point calculation uses 360 nm - 780 nm range
     * at either 1 nm or 5 nm intervals (> 5 nm requires different procedures)
     * following ASTM standard practice https://doi.org/10.1520/E0308-18)
     * however, SPD definition can cover a wider range
     * e.g.
     * - Illuminant D starts from 300 nm; < 360 nm values are ignored
     * - Illuminant F starts from 380 nm; 360 - 380 nm values are treated as 0
     */
    internal const int StartWavelength = 360;
    internal const int EndWavelength = 780;
    private const int WavelengthCount = EndWavelength - StartWavelength + 1;
    
    public int WavelengthDelta => GetWavelengthDelta();
    public bool IsValid => GetIsValid();

    public Spd()
    {
    }
    
    public Spd(Dictionary<int, double> dictionary)
    {
        foreach (var pair in dictionary)
        {
            Add(pair.Key, pair.Value);
        }
    }

    public double SpectralPower(int wavelength)
    {
        return TryGetValue(wavelength, out var value) ? value : 0;
    }
    
    private int GetWavelengthDelta()
    {
        var wavelengths = Keys.OrderBy(x => x).ToList();
        if (wavelengths.Count < 2) return 1; // assumes every wavelength at 1 nm interval has 0 power
        return wavelengths[1] - wavelengths[0]; // assumes even distribution, which can be confirmed using IsValid()
    }
    
    private bool GetIsValid()
    {
        var wavelengths = Keys.OrderBy(x => x).ToList();
        if (wavelengths.Count == 1) return true; // assumes every wavelength at 1 nm interval has 0 power
        
        var deltas = wavelengths.Zip(wavelengths.Skip(1), (first, second) => second - first).Distinct().ToList();
        if (deltas.Count != 1) return false;

        var delta = deltas.Single();
        return delta is 1 or 5;
    }

    public static List<int> ExpectedWavelengths(int interval)
    {
        var range = Enumerable.Range(StartWavelength, WavelengthCount);
        return range.Where(wavelength => wavelength % interval == 0).ToList();
    }

    public override string ToString() => this.Any() ? $"{this.First().Key} nm to {this.Last().Key} nm by {WavelengthDelta} nm" : "(no wavelength data)";

